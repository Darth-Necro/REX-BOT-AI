function TabContent({ id }) {
  switch (id) {
    case 'overview': return <OverviewPanel />;
    case 'devices': return <DevicePanel />;
    case 'threats': return <ThreatPanel />;
    case 'network': return <NetworkMapPanel />;
    case 'firewall': return <FirewallPanel />;
    case 'settings': return <SettingsPanel />;
    case 'logs': return <LogViewer />;
    case 'chat': return <RexChat />;
    default: return null;
  }
}

export default function AdvancedView() {
  const [activeTab, setActiveTab] = useState('overview');

  return (
    <div className="min-h-[calc(100vh-4rem)]">
      {/* Tab bar */}
      <div className="border-b border-rex-card flex items-center justify-between overflow-x-auto bg-rex-surface/50">
        <div className="flex" role="tablist">
          {tabs.map((tab) => {
            const Icon = tab.icon;
            return (
              <button
                key={tab.id}
                role="tab"
                aria-selected={activeTab === tab.id}
                className={`flex items-center gap-2 px-4 py-3 text-sm font-medium whitespace-nowrap transition-colors ${
                  activeTab === tab.id
                    ? 'text-rex-accent border-b-2 border-rex-accent'
                    : 'text-rex-muted hover:text-rex-text'
                }`}
                onClick={() => setActiveTab(tab.id)}
              >
                <Icon className="w-4 h-4" />
                <span className="hidden sm:inline">{tab.label}</span>
              </button>
            );
          })}
        </div>
        <div className="hidden sm:flex items-center pr-4">
          <ConnectionIndicator />
        </div>
      </div>

      {/* Tab content */}
      <div className="p-4">
        <Suspense fallback={<LoadingFallback />}>
          <TabContent id={activeTab} />
        </Suspense>
      </div>
    </div>
  );
}
